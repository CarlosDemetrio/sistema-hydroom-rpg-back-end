/**
 * Status de uma ficha de personagem.
 *
 * <ul>
 *   <li>RASCUNHO — ficha recém-criada, ainda sem todos os campos obrigatórios preenchidos</li>
 *   <li>COMPLETA — ficha validada e pronta para uso em sessão</li>
 * </ul>
 */
public enum FichaStatus {
    RASCUNHO,
    COMPLETA
}
